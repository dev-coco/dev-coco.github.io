const elements = document.querySelectorAll('[data-localize]')
let browserLang = navigator.language || navigator.userLanguage
browserLang = browserLang.toLowerCase()
const currentLang = browserLang.startsWith('zh') ? 'zh' : 'en'

const localize = localizeLanguage[currentLang]

for (const el of elements) {
  const key = el.getAttribute('data-localize')
  const text = localizeLanguage[currentLang][key]
  if (!text) continue
  if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
    el.placeholder = text
  } else {
    el.innerText = text
  }
}
